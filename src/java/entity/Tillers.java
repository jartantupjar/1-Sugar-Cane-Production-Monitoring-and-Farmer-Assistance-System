public class Tillers {
    private int year,fields_id,rep,count;

    /**
     * @return the year
     */
    public int getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year) {
        this.year = year;
    }

    /**
     * @return the fields_id
     */
    public int getFields_id() {
        return fields_id;
    }

    /**
     * @param fields_id the fields_id to set
     */
    public void setFields_id(int fields_id) {
        this.fields_id = fields_id;
    }

    /**
     * @return the rep
     */
    public int getRep() {
        return rep;
    }

    /**
     * @param rep the rep to set
     */
    public void setRep(int rep) {
        this.rep = rep;
    }

    /**
     * @return the count
     */
    public int getCount() {
        return count;
    }

    /**
     * @param count the count to set
     */
    public void setCount(int count) {
        this.count = count;
    }
}
